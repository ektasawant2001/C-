//Write a C++ Program to find Square Root of a number using sqrt() function.
#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    int n,r;
    cout<<"Enter a number: "<<endl;
    cin>>n;
    r=sqrt(n);
    cout<<"The square root of "<<n<<" is: "<<r<<endl;
    return 0;

}
